class Command(BaseCommand):
    help = "更新所有關鍵字的定義欄位"

    def handle(self, *args, **options):
        updated = 0
        skipped = 0
        not_found = []

        for kw_id, definition in DEFINITIONS.items():
            try:
                kw = Keyword.objects.get(pk=kw_id)
                kw.definition = definition
                kw.save(update_fields=["definition"])
                updated += 1
            except Keyword.DoesNotExist:
                not_found.append(kw_id)
                skipped += 1

        self.stdout.write(self.style.SUCCESS(f"完成：更新 {updated} 筆，跳過 {skipped} 筆"))
        if not_found:
            self.stdout.write(self.style.WARNING(f"找不到的 ID：{not_found}"))
